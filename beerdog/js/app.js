function sendToCart(){
  document.getElementById('btn').value="added to cart";
  document.getElementById('btn').disabled=true;
  var beername=$('.view-name').text()
  var beerprice=$('.view-price').html().replace('$ ','')
  
  addToCart(beername,beerprice);

  sleep(2000).then(() => {
    document.getElementById('btn').value="purchase";
  document.getElementById('btn').disabled=false;
});
  
  
}


function sleep (time) {
  return new Promise((resolve) => setTimeout(resolve, time));
}


